
public class Variable1 {

	public static void main(String[] args) {
		int num = 100;
		double num2 = 22.2;
		char c = 'a';
		boolean b = true;
		
		
		System.out.println("num = " + num);
		System.out.println("num2 = " + num2);
		System.out.println("c = " + c);
		System.out.println("b = " + b);
		
		num = 200;
		System.out.println("num = " + num);
		
	}

}
