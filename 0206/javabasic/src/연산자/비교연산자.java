package 연산자;

public class 비교연산자 {

	public static void main(String[] args) {
		//비교연산자 ( == != > >=)		
		int x = 20;
		int y = 10;
		System.out.println();
		System.out.println("x == y ? " + (x == y));
		System.out.println("x != y ? " + (x != y));
		System.out.println("x > y ? " + (x > y));
		System.out.println("x >= y ? " + (x >= y));

	}

}
